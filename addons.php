<h1>Addons and Plugins for WingsXI</h1>

<p>Many users will notice that Ashita requires auto updates enabled to use their Addons/Plugins tab.  However, turning on Ashita auto updates will break certain addons due to Wings using an older client version than Ashita supports.  We solved this problem for ourselves by creating a launcher that keeps all files on the version we support.</p>

